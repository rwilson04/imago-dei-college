     <h1 style="text-align: center;">What does an average night look like?</h1>
      </div>
     
      <div class="span11 alert alert-block">
      <p>We rendezvous every Friday night, from 7-9:30 p.m. to, at times, engage in a brief presentation of theology, philosophy, or biblical teaching from professors, pastors and other speakers. At other times, share meals, hang out and talk, watch and discuss movies, and so on. We're big on switching things up a lot.</p>

<p>We also encourage you to stay late with us.  We'll keep the lights on until 10:00 and the world is your oyster.  Take avantage of the gym for a game of dodgeball or ask nicely and maybe we'll even throw a log in the fire.  It's during this downtime that we have the opportunity to build spiritual friendships, even if it means taking a kickball to the face.</p>
